                            <div class="contentTitle" style="margin-top: 0;">PDF</div>
                            <div class="textContent">

                                Le Portable Document Format est le format de livre le plus répandu aujourd’hui.<br/><br/>

                                Il a la particularité de conserver la mise en forme d’un fichier. Polices d’écritures, images,
                                objets graphiques, s’affichent comme l’auteur du document les a définis quel que soit le logiciel
                                d’exploitation et l’ordinateur utilisé pour l’imprimer ou le visualiser.<br/><br/>

                                Pour la lecture numérique, cette spécificité est aussi sa principale limite. La mise en page statique
                                du PDF se désunit lorsque l’écran trop petit ne permet pas de le visualiser à l’échelle 1 et que l’on
                                doit grossir le document pour le lire convenablement. Défaut gênant sur les liseuses de type encre
                                électronique. La mise en page PDF est mieux supportée par les tablettes. Plus puissantes, elles
                                permettent de zoomer lorsque l'écran est trop petit pour lire confortablement.<br/><br/>

                                Le PDF est consultable sur un grand nombre d’appareils informatiques (ordinateurs, tablettes,
                                liseuses, téléphones mobiles…).<br/><br/>

                                Pour le lire sur votre ordinateur, il vous faudra télécharger le lecteur gratuit d’Adobe « Acrobat
                                Reader » pour <a class="textLinks" href="http://get.adobe.com/fr/reader/" target="_blank">Windows</a> ou pour <a class="textLinks" href="http://www.clubic.com/telecharger-fiche18906-adobe-reader-acrobat.html" target="_blank">Mac</a>. Pour les appareils mobiles, le dispositif est déjà contenu dans vos
                                applications de lecture (iBooks, Aldiko…).<br/>

                                <div class="grid_2">
                                    <div class="forts">Points forts</div>

                                    - Très large diffusion du format, compatible avec un très grand nombre de systèmes d’exploitation.
                                </div>

                                <div class="grid_2">
                                    <div class="faibles">Points faibles</div>

                                    - Peu adapté à la lecture sur liseuse à encre électronique.<br/>

                                    - Format propriétaire
                                </div>

                            </div>

