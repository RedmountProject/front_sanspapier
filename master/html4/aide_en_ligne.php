                            <div class="contentTitle">Qu'est ce que c'est ?</div>

                            <div class="textContent">
                                La Gestion des Droits Numériques (Digital Rights Management, DRM en anglais) a pour objectif
                                de contrôler par des mesures techniques de protection, l'utilisation qui est faite des œuvres
                                numériques.<br/><br/>

                                Ces dispositifs techniques ou logiciels peuvent viser à :<br/><br/>

                                - Restreindre la lecture du support à une zone géographique prévue (par exemple les zones des DVD)<br/>

                                - Restreindre la lecture du support à du matériel spécifique (ex les versions smartphone ou tablette)<br/>

                                - Restreindre la lecture du support à un constructeur ou vendeur (afin de bloquer la concurrence)<br/>

                                - Restreindre ou empêcher la copie privée du support (transfert vers un appareil externe)<br/>

                                <a class="textLinks" style="float: right; font-size:12px;" href="http://fr.wikipedia.org/wiki/Gestion_des_droits_num%C3%A9riques" target="_blank">Source Wikipédia</a><br/><br/>

                                Concrètement, l’existence de DRM sur votre livre numérique limite le nombre de copies possibles sur
                                des appareils différents.<br/><br/>

                                Pour lire vos fichiers téléchargés équipés de DRM, vous devez obligatoirement utiliser le logiciel
                                Adobe Digital Editions. Il permet de gérer des bibliothèques de livres numériques sur votre
                                ordinateur et vos appareils de lecture mobile compatibles (liseuses, tablettes, téléphones).<br/><br/>

