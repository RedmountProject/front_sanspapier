                                Sa mise en page s’adapte au support de réception. Elle s'adapte aussi à la taille de la police choisie. On le consulte aussi facilement sur liseuse que
                                sur tablette.<br/><br/>

                                A l’exception du Kindle d’Amazon, il est compatible avec toutes les tablettes du marché.<br/><br/>
                                Pour lire les ePub sur votre ordinateur, nous vous recommandons d’utiliser le logiciel Calibre.
                                Libre et mis à jour régulièrement, il est sans doute le logiciel le plus adapté pour votre lecture sur ordinateur. 
                                Il bénéficie en outre de nombreuses fonctionnalités de conversion entre de multiples formats de fichier (bien pratique lorsqu’il faut changer d’appareil).<br/>

                                Il est téléchargeable <a class="textLinks" href="http://calibre-ebook.com/download" target="_blank">ici</a>.<br/>

